public class a_05_operators {
    public static void main(String[] args) {
        int a=4;
        int b=6+a;
        System.out.println(b);
        System.out.println(a==b);
        System.out.println(20>2 && 30<60);
        System.out.println(20>2 || 30<60); //or operator

    }
}
